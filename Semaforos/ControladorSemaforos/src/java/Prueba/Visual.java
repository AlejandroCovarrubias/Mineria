    public Visual() {
        initComponents();
        
        this.setLocationRelativeTo(null);
        ProducerControl producer = new ProducerControl();
        Routeador routeador = new Routeador(producer, this);
        ConsumerControl consumer = new ConsumerControl(routeador);
        
        Thread consumidor = new Thread(consumer);
        consumidor.start();
        
        semaforos = routeador.getIdSemaforos();
        
        paneles.add(pnl1);
        paneles.add(pnl2);
        paneles.add(pnl3);
        
        for (int i = 0; i < semaforos.size(); i++) {
            VisualSemaforo visual = new VisualSemaforo(producer, semaforos.get(i));
            visuales.add(visual);
            visual.setSize(paneles.get(i).getSize());
            visual.setBorder(paneles.get(i).getBorder());
            visual.setVisible(true);
            paneles.get(i).add(visual);
            paneles.get(i).revalidate();
            paneles.get(i).repaint();
        }
    }
    
    public void actualizarVisualSemaforo(String visual, String estado){
        for (VisualSemaforo visuals : visuales) {
            if(visuals.getNombre().equals(visual)){
                visuals.setEstado(estado);
            }
        }
    }

